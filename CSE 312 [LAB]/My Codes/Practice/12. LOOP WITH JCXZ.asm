.MODEL SMALL
.STACK 100H
.DATA
.CODE

    MAIN PROC

MOV CX,0       ;CX HOLDS INPUT COUNTER

MOV AH,2       ;REQUEST INPUT
MOV DL,'*'

JCXZ SKIP      ;JUMP IF CX IS ZERO

TOP:
INT 21H        ;LOOPING THE DISPLAY

LOOP TOP       ;CALLING THE LOOP FUNCTION

SKIP:
MOV AH,4CH
INT 21H

    MAIN ENDP
       
END MAIN           