.MODEL SMALL
.STACK 100H
.DATA
.CODE

    MAIN PROC
 
MOV AH,1      ;TAKE INPUT
INT 21H

CMP AL, 'Y'   ;IF INPUT CHARACTER IS EQUAL TO Y OR
JE DISPLAY

CMP AL, 'y'   ;IF INPUT CHARACTER IS EQUAL TO y
JE DISPLAY

JMP RETURN    ;NOTHING MATCHES? JUMP AND DISPLAY NOTHING!

DISPLAY:
MOV AH,2      ;ONE COMPARISON IS SUCCESSFUL SO DISPLAY THIS I MEAN DISPLAY INPUT CHARACTER
MOV DL,AL
INT 21H

RETURN:
MOV AH,4CH    ;DISPLAYS NOTHING SINCE Y/y DOESNT MATCHES
INT 21H    

     MAIN ENDP

END MAIN







    
 
MAIN ENDP

END MAIN