.MODEL SMALL
.STACK 100H
.DATA
.CODE

    MAIN PROC
 
MOV AH,1      ;TAKE INPUT
INT 21H

CMP AL, 'A'   ;IF INPUT CHARACTER IS NOT GRATER THAN OR EQUAL TO A
JNGE RETURN

CMP AL, 'Z'   ;IF INPUT CHARACTER IS NOT LESS THAN OR EQUAL TO Z
JNLE RETURN

MOV AH,2      ;COMPARISONS ARE IN VEIN SO DISPLAY THIS I MEAN DISPLAY UPPERCASE
MOV DL,AL
INT 21H

RETURN:
MOV AH,4CH    ;DISPLAYS NOTHING SINCE LETTER IS NOT UPPERCASE
INT 21H    

     MAIN ENDP

END MAIN







    
 
MAIN ENDP

END MAIN