.MODEL SMALL
.STACK 100H
.DATA
.CODE

    MAIN PROC

MOV AX,-1    ;DEFAULT VALUE
CMP AX,0     ;COMPARE WHEN AX HAS 0 IN IT

JL NEGATIVE  ;JUMP IF AX IS LESS THAN 0
JE ZERO      ;JUMP IF AX EQUALS TO ZERO
JG POSITIVE  ;JUMP IF AX IS GREATER THAN ZERO

NEGATIVE:
MOV BX,-1    ;-1<0 SO COME HERE AND STORE -1 IN BX AND RETURN!
JMP RETURN

ZERO:
MOV BX,0
JMP RETURN

POSITIVE:
MOV BX,1
JMP RETURN

RETURN:
MOV AH,4CH
INT 21H

     MAIN ENDP

END MAIN







    
 
MAIN ENDP

END MAIN