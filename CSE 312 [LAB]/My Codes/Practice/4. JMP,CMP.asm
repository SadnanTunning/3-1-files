.MODEL SMALL
.STACK 100H
.DATA


A DW 5
B DW 3

.CODE

MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    
    MOV AX,A
    MOV BX,B
    CMP AX,BX      ;IS AX IS GREATER THAN BX?
    JG  LEVEL1
    
    MOV CX,BX      ;NOT GRATER FOR THIS EXAMPLE, SO STORE NOTHING
    JMP RETURN     ;UNCONDITIONAL JUMP
    
    LEVEL1:
    MOV CX,AX      ;STORE AX TO CX  
    
    RETURN:
    MOV AH,4CH
    INT 21H
    
MAIN ENDP

END MAIN