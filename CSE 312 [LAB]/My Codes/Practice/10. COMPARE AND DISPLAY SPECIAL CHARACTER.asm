.MODEL SMALL
.STACK 100H
.DATA
.CODE

    MAIN PROC
 
MOV CX,61H

CMP CX,'a'    ;COMPARE CX=61H WITH a CHARACTER

JE a          ;MATCHES SO JUMP

MOV AL,'*'    ;NOT EXECUTED
MOV AH,0EH
INT 10H

a:
MOV AL,'+'   ;DISPLAY "+" SIGN
MOV AH,0EH
INT 10H

     MAIN ENDP

END MAIN

    
 
   MAIN ENDP

END MAIN



