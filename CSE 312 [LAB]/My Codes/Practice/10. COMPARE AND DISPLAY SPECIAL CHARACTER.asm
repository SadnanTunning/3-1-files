.STACK 100H
.DATA
.CODE

    MAIN PROC
 
MOV CX,5H
MOV BX,6H

CMP CX,BX

JE a          ;IN ASCII 5H,6H HAS SPECIAL CHARACTER, IT DOESNT MATCHES WITH a WHICH IS IN 97H

MOV AL,'*'    ;DISPLAYS SPECIAL CHARACTER
MOV AH,0EH
INT 10H

a:            ;LANDING,NOTHING

     MAIN ENDP

END MAIN







    
 
MAIN ENDP

